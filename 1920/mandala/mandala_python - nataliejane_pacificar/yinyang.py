import turtle as nat

def pattern(clr1, clr2):
    nat.speed(0)
    nat.width(10)
    nat.color("black", clr1)
    nat.begin_fill()
    nat.circle(150., 180)
    nat.circle(300,180)
    nat.left(180)
    nat.circle(-150,180)
    nat.end_fill()
    nat.left(90)
    nat.up()
    nat.forward(300*0.35)
    nat.right(90)
    nat.down()
    nat.color(clr1,clr2)
    nat.begin_fill()
    nat.circle(300*0.15)
    nat.end_fill()
    nat.left(90)
    nat.up()
    nat.backward(300*0.35)
    nat.down()
    nat.left(90)